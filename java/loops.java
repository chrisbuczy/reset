public class loops {
    
    public static void main(String[] args) {

        int x = 5;

        // while loop
        while(x != 0) {
            System.out.println("x does not equal 0");
            x--;
        }

        // for loop

        for(int i = 0; i <= 10; i++) {
            System.out.println(i);
        }

    }
}
