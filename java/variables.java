public class variables {

    public static void main(String[] args) {

        // there is char, string, int, long, short, float, double, boolean
        // put f at end of float and d at end of double
        String y = "hello world";
        long long_num = 1112342;
        float decimal = 3.14f;
        double ddecimal = 3.14159d;
        System.out.println(y);
        System.out.println(long_num);
        System.out.println(decimal);
        System.out.println(ddecimal);


    }
}